class CoffeeMaker{
	static boolean isCoffeeMakerOn;
	static boolean onOrOff() {
		if(isCoffeeMakerOn == false) {
			isCoffeeMakerOn = true;
			System.out.println("The coffee maker is ON");
		}
		else if(isCoffeeMakerOn = true) {
			isCoffeeMakerOn = false;
			System.out.println("The coffee maker is OFF");
		}
		return isCoffeeMakerOn;
			
	}
}